from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware

app = FastAPI(title="Minha API", version="1.0.0")

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],   # depois restrinja ao(s) seu(s) dom√≠nio(s)
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

@app.get("/health")
def health():
    return {"status": "ok"}

@app.get("/echo")
def echo(msg: str = "hello"):
    return {"echo": msg}
